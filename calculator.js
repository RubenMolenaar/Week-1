var a = parseInt(prompt("voer je eerste nummer in"));
var x = prompt("voer hier je muliplyer in");
var b = parseInt(prompt("voer je tweede numme in"));
if (x == "*") {
	alert(a * b)
}
else if(x == "/") {
	alert(a / b)
}
else if(x == "+") {
	alert(a + b)
}
else if(x == "-") {
	alert(a - b)
}
